package com.haruntasci.recyclerviewgridproject;

public class Physicists {

    private int image;
    private String name;

    public Physicists(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
